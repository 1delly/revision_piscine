#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	a;

	a = 0;
	(void)argc;
	ft_putstr(argv[a]);
	write(1, "\n", 1);
	a++;
}
